package io.soabase.maple;

import io.soabase.maple.api.annotations.DoNotLog;

public class Address {
    private final String street;
    private final String city;
    private final String state;
    private final String zip;
    @DoNotLog private final String secret;

    public Address(String street, String city, String state, String zip, String secret) {
        this.street = street;
        this.city = city;
        this.state = state;
        this.zip = zip;
        this.secret = secret;
    }

    public String getStreet() {
        return street;
    }

    public String getCity() {
        return city;
    }

    public String getState() {
        return state;
    }

    public String getZip() {
        return zip;
    }

    public String getSecret() {
        return secret;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        Address address = (Address) o;

        if (street != null ? !street.equals(address.street) : address.street != null) return false;
        if (city != null ? !city.equals(address.city) : address.city != null) return false;
        if (state != null ? !state.equals(address.state) : address.state != null) return false;
        if (zip != null ? !zip.equals(address.zip) : address.zip != null) return false;
        return secret != null ? secret.equals(address.secret) : address.secret == null;
    }

    @Override
    public int hashCode() {
        int result = street != null ? street.hashCode() : 0;
        result = 31 * result + (city != null ? city.hashCode() : 0);
        result = 31 * result + (state != null ? state.hashCode() : 0);
        result = 31 * result + (zip != null ? zip.hashCode() : 0);
        result = 31 * result + (secret != null ? secret.hashCode() : 0);
        return result;
    }

    @Override
    public String toString() {
        return "Address{" +
                "street='" + street + '\'' +
                ", city='" + city + '\'' +
                ", state='" + state + '\'' +
                ", zip='" + zip + '\'' +
                ", secret='" + secret + '\'' +
                '}';
    }
}
